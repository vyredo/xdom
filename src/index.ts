// Populate xdom with element names: xdom.div, xdom.title, etc.
type ElementName = (typeof elementNames)[number] & keyof HTMLElementTagNameMap;
const xdom: Record<ElementName, XDOMOverload<ElementName>> = elementNames.reduce((prev, elementName) => {
  prev[elementName as ElementName] = createDomElementArgumentHandler(elementName as ElementName);
  return prev;
}, {} as Record<ElementName, XDOMOverload<ElementName>>);

